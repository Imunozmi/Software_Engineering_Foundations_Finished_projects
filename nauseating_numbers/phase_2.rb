
def anti_prime? (num)
    top_num = 0
    top_count = 0

    (1..num).each do |n|
        cont = (1..n).count { |fact| n % fact == 0 }
        if cont > top_count
            top_num = n
            top_count = cont 
        end
    end

    top_num == num
end

